			<p style="font-size:19px;padding-top:40px;">Corfu is famous for being the greenest Greek island and nature-lovers instantly fall in love with Corfu’s countryside.


			<br><br>The island’s natural landscape runs the gamut from lagoons and lakes to valleys and mountains. Some of the most unique natural phenomena can be found in Corfu, including a lake that borders a desert which in turn boasts juniper-tree thickets.


			<br><br>Corfu's favourable climate and winter rainfall ensures its verdant nature throughout the year.


			<br><br>The combination of fertile land, plentiful rains and mild weather have maintained this island so lush, it has the richest flora of all the Mediterranean islands with over 6000 documented species.


			<br><br>The fields and hills are also rich with trees other than olives. There are also massive oaks and elms, untouched by the ravages of disease. Everywhere the dark green cypress trees pierce the olive groves adding yet another dimension to the landscape. As you walk through the island you will also come upon scented, wild herbs growing by the pathways.


			<br><br>These range from rosemary, mint, chamomile, fennel, borage, marjoram, bay, and oregano to sage, thyme and garlic.


			<br><br>Fruit abounds and during the appropriate season you can feast as you walk on almonds, figs, pomegranates, even quince, cherries, pears and grapes.


			<br><br>Part of the Natura 2000 network and a thriving and vital ecosystem, Korission Lake in the south-western part of the island is an important stop for migrating birds on their journey from Africa to Europe. Over 2000 birds seek refuge in the Lake, including a wide variety of water birds, flamingos, and swans as well as falcons. Over 150 species of birds have been sighted in Corfu, either as residents or migrants. The Glossy Ibis, the Spoonbill, the Gull-Billed Tern, the Great White Egret and the Pygmy Cormorant are some of the rarest.


			<br><br>In a close distance discover Cedar forest a truly unique phenomenon, where thickets of small cypress-like trees that belong to the Juniperus family sprout from the sand to compose a stunning, one-of-a-kind landscape.


			<br><br>Another important habitat for Corfu’s bird population is Antinioti Lagoon in the north with over 96 rare species sighted here. This brackish lagoon is also part of the Natura 2000 network.


			<br><br>The Ropa Valley, which stretches across a plain of about 2,500 acres in central Corfu, is an important wetlands habitat, home to a multitude of birds, reptiles, amphibians and fish. The amphibians include freshwater terrapins, which can be seen in the river which runs beside the golf course, along with hundreds of bullfrogs. There are also many kinds of small mammal such as weasels, foxes, hedgehogs and otters. There are also many spectacular butterflies including the Swallowtail, the tiny Common Blue and the magnificent Two-Tailed Pasha.


			<br><br>Mount Pantokrator in the north is the island’s highest peak at 918m, an interesting part of The Corfu Trail. Walking on the trail at higher levels in the beginning of summer, one can see species which flowered earlier at lower altitudes. Agii Deka and Merovigli mountains complete the island’s tableau of picturesque landscapes.
