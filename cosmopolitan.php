					<p style="">Corfu, the Grand Lady of the Ionian, is identified by a series of mythical images: Nausica, the daughter of King Alkinoos, the man who saved Odysseus when he was shipwrecked in the Country of Phaeacians (which many have identified as the island of Corfu).
Sissi, the sad Empress of Austria and the Achilleion palace she built. Majestic images Mon Repos, the 19thcentury summer residence built for the British High Commissioner; the Kaiser's Observatory, where Wilhelm II looked out at the sea.
Equally majestic are the iconic city mansions of Corfu, the Liston Arcade and Spianada Square - the largest in the Balkans.
Venetians, English, French, Russians and Greeks: all lived and flourished here, and left their traces on the islands numerous sights and attractions. They sculpted a diverse culture that you will discover in every musical note of Corfu's marching bands, in each glass of kumquat liqueur, in the labyrinthine alleyways of Campiello. Corfu will seduce you.
					</p>

					<h2 style="font-size:30px; font-weight:100;">Corfu's historic centre</h2>
					<p style="">Unesco has declared the island's historic centre a world heritage site. The stately buildings with neoclassical influences will dazzle you. The Palace of St. Michael and St. Georgiou, the Liston Arcade, the Reading Society, the Catholic cathedral, the Maitland rotunda, the Ionian Parliament, the Kapodistrias family mansion. In the famous alleyways of Campiello, with clotheslines hanging above you, and in the opulent hotels with their VIP guests, you will feel like a star in a period movie. </p>

					<h2 style="font-size:30px; font-weight:100;">The city's keepers</h2>
					<p style="">On the eastern side of the city, the rocky shore crowned by two low peaks gave the name Koryfo - Corfu (meaning peak) to the island. You enter the Old Fort (Palaio Frourio), built in the 13th century, via a bridge over a seawater moat. It includes the Porta Sopranza, the British barracks, two winged lions of St Mark, the Madonna del Carmine, the Savorgnan bastion, the Tower of Sea and the Tower of the Land (Castel del Mar, Castel della Terra), and the British-built church of St George which resembles a Doric temple.
						It's an open-air museum, and each sight is a journey through time. Towering above the northwest border of the capital, the other guardian, the Neo Frourio (New Fort), is a masterpiece of military architecture built by the Venetians between 1572 and 1645. It is a fortress with planes and angles reminiscent of Picasso and Braque's Cubist periods.</p>

					<h2 style="font-size:30px; font-weight:100;">Photogenic attractions</h2>
					<p style="">The trademarks of Corfu are actually other islands: the diminutive Pontikonisi, which translates as Mouse Island, just large enough to hold the Pantokrator monastery. Right next to it, the famous islet Vlacherna, with its 17th century monastery dedicated to the Virgin Mary, is connected to the mainland by a narrow causeway. Mass is held here on Easter Mond</p>

					<h2 style="font-size:30px; font-weight:100;">Mythical resorts</h2>
					<p style="">One of the most famous tourist resorts of Corfu, Paleokastritsa, the lush greenery blankets six rocky coves. Some scholars maintain that this was the site of the mythical palace of King Alkinoos, where Odysseus stayed. The Monastery of Panagias was founded in 1225. It reaches as far as the famous Angelokastro, built early in the 12th century, with its church of Agia Kyriaki hidden in a small cave.</p>

					<h2 style="font-size:30px; font-weight:100;">Easter in Corfu</h2>
					<p style="">A popular destination in Greece, Corfu gives you so many reasons to visit it. One of these is its very special, and world-famous, Easter celebration. The Philharmonic Society of Corfu accompanies the procession of the mummified body of the island's patron, St Spyridon, as it is carried around the city on Good Friday. On Holy Saturday it rains botides - clay jars thrown in the streets from people's balconies. Don't get too close! Easter in Corfu is something you should experience at least once in your lifetime.
					</p>

					<h2 style="font-size:30px; font-weight:100;">Achillion Palace</h2>
					<p style="">The island's imperial past could be the reason that Corfu is considered one of the most glamorous and luxurious destinations in Greece. Empress Elizabeth of Austria-Hungary, the legendary Sissi, chose Corfu to build her palace, a place where she found consolation after her son Rudolph's suicide. It is an elegant Pompeian-style building with luxurious, neoclassical living rooms. In the beautiful gardens you'll admire the famous statue of Dying Achilles.
					</p>

					<h2 style="font-size:30px; font-weight:100;">The dream of an aristocrat</h2>
					<p style="">British High Commissioner Frederick Adam built this summer residence for his Corfiot wife, Nina Palatianou. Situated in a landscape of rare beauty, it occupies the site of ancient Corcyra, as the city was then called. The villa houses the Palaiopolis museum and botanical gardens with more than 2,000 rare kinds of plants that came all the way from the British colonies.
					</p>

					<h2 style="font-size:30px; font-weight:100;">Beaches of all kinds
					</h2>
					<p style="">Barbati, Nissaki, Kalami, Kouloura, Kerasia, Agios Stefanos, Glyfada, Dassia, Ermones, Paleokastritsa, Gape Drastis, Kontokali, Benitses, Halikounas. The list of beaches is endless.
					Isolated picturesque coves or large and crowded beaches with beach bars, tavernas, shops and water sports are waiting you to discover them
					</p>

					<h2 style="font-size:30px; font-weight:100;">The footpaths of Corfu
					</h2>
					<p style="">Get to know the island's rich flora and fauna and old settlements by foot or bicycle. Among the hidden gems are: Pantokratoras, the southern part Corfu next to the saltwater lake of Korission, the area of Palaiokastritsas, and the northern side of the island towards the saltwater lake Antinioti - all wonderful routes that will fill your holidays with unique moments.
					</p>

